package org.nisheeth.ejb.entity;

import java.io.Serializable;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.ManyToOne;
import javax.persistence.OneToOne;

/**
 *
 * @author Nisheeth Shah
 */
@Entity
public class UserEntity implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;
    private String firstName;
    private String lastName;
    @Column(unique = true)
    private String email;
    @ManyToOne
    private ACLRoleEntity aCLRoleEntity;
    @OneToOne
    private LoginDetailEntity loginDetailEntity;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public ACLRoleEntity getaCLRoleEntity() {
        return aCLRoleEntity;
    }

    public void setaCLRoleEntity(ACLRoleEntity aCLRoleEntity) {
        this.aCLRoleEntity = aCLRoleEntity;
    }

    public LoginDetailEntity getLoginDetailEntity() {
        return loginDetailEntity;
    }

    public void setLoginDetailEntity(LoginDetailEntity loginDetailEntity) {
        this.loginDetailEntity = loginDetailEntity;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        if (!(object instanceof UserEntity)) {
            return false;
        }
        UserEntity other = (UserEntity) object;
        return !((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id)));
    }

    @Override
    public String toString() {
        return "UserEntity{" + "id=" + id + ", firstName=" + firstName + ", lastName=" + lastName + ", email=" + email + ", aCLRoleEntity=" + aCLRoleEntity + ", loginDetailEntity=" + loginDetailEntity + '}';
    }

}
